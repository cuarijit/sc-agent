export function resolveIcon(iconName: string): SvgIconComponent {
  if (!iconName) return InsightsOutlinedIcon;
  // Accept both forms: "DashboardOutlined" (registry key) and
  // "DashboardOutlinedIcon" (the MUI export name used in seed data).
  const stripped = iconName.endsWith("Icon") ? iconName.slice(0, -4) : iconName;
  return ICON_REGISTRY[iconName] ?? ICON_REGISTRY[stripped] ?? InsightsOutlinedIcon;
}

export function getAvailableIconNames(): string[] {
  return Object.keys(ICON_REGISTRY);
}
